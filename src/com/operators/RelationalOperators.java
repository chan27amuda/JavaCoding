package com.operators;

public class RelationalOperators {

	public static void main(String[] args) {
		// Relational Operators!! Used mainly on Primitive Data Types (Not on boolean
		// data type) and Numbers
		// >; <; >=; <=; ==; !=

		int number1 = 5;
		int number2 = 10;
		System.out.println(number1 > number2);// OP: false
		System.out.println(number1 < number2);// OP: true
		System.out.println(number1 == number2);// OP: false
		System.out.println(number1 != number2);// OP: true
		System.out.println(number1 >= number2);// OP: false
		System.out.println(number1 <= number2);// OP: true

	}

}
