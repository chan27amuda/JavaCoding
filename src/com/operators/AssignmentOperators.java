package com.operators;

public class AssignmentOperators {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;

		System.out.println(a += b); // same as --> a = a + b
		System.out.println(a -= b); // same as --> a = a - b
		System.out.println(a *= b); // same as --> a = a * b
		System.out.println(a /= b); // same as --> a = a / b
		System.out.println(a %= b); // same as --> a = a % b

	}

}
